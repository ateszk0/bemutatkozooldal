// Scroll Animation
const animateOnScroll = () => {
    const elements = document.querySelectorAll('.hero-title, .hero-subtitle, .cta-btn, .image-wrapper');
    
    elements.forEach(element => {
        const elementPosition = element.getBoundingClientRect().top;
        const screenPosition = window.innerHeight / 1.3;
        
        if (elementPosition < screenPosition) {
            element.style.opacity = '1';
            element.style.transform = 'translateY(0)';
        }
    });
};

// Set initial state for animation
window.addEventListener('load', () => {
    document.querySelector('.hero-title').style.opacity = '0';
    document.querySelector('.hero-title').style.transform = 'translateY(20px)';
    document.querySelector('.hero-subtitle').style.opacity = '0';
    document.querySelector('.hero-subtitle').style.transform = 'translateY(20px)';
    document.querySelector('.hero-cta').style.opacity = '0';
    document.querySelector('.hero-cta').style.transform = 'translateY(20px)';
    document.querySelector('.image-wrapper').style.opacity = '0';
    document.querySelector('.image-wrapper').style.transform = 'scale(0.9)';
    
    setTimeout(() => {
        animateOnScroll();
    }, 300);
});

window.addEventListener('scroll', animateOnScroll);

// Tab functionality for Experience page
const tabBtns = document.querySelectorAll('.tab-btn');
const tabContents = document.querySelectorAll('.tab-content');

if (tabBtns.length > 0) {
    tabBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const tabId = btn.getAttribute('data-tab');
            
            // Remove active class from all buttons and contents
            tabBtns.forEach(btn => btn.classList.remove('active'));
            tabContents.forEach(content => content.classList.remove('active'));
            
            // Add active class to clicked button and corresponding content
            btn.classList.add('active');
            document.getElementById(tabId).classList.add('active');
        });
    });
}

